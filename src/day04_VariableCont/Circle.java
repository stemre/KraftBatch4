package day04_VariableCont;

public class Circle {
    public static void main(String[] args) {


        double r = 5.5;
        double PI = 3.14;

        double area = PI * r * r;
        double perimeter = 2 * PI * r;

        System.out.println("PI =" + PI);
        System.out.println("r =" + r);
        System.out.println("area =" + area);
        System.out.println("perimeter =" + perimeter);
    }
}
