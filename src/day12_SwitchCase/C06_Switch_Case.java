package day12_SwitchCase;

public class C06_Switch_Case {
    public static void main(String[] args) {


        System.out.println("giriş");
        int sayi = 2;

        switch (sayi) {
            case 1:
                System.out.println("sayı 1e esıttır");
                break;
            case 2:
                System.out.println("sayı 2ye esittir");
                break;
            case 3:
                System.out.println("sayı 3e esittır");
                break;
            default:
                System.out.println("sayı eslesmedı");


        }


    }
}
